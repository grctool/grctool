func TestGenerateSimpleEvidence_Basic(t *testing.T) {
	t.Parallel()
	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator("/tmp/data", log)

	task := &domain.EvidenceTask{
		ReferenceID: "ET-0001",
		Name:        "GitHub Repository Access Controls",
		Description: "Verify team permissions for all repositories.",
	}

	tasks := []CollectionTask{
		{
			Number:      1,
			Description: "Collect repository permissions",
			Evidence:    "Teams and access levels documented.",
			Snippets: []EvidenceSnippet{
				{
					Content:    "Team: Engineering\nAccess: Write",
					SourceFile: "permissions.json",
					OriginalPath: "docs/evidence/permissions.json",
					LastModified: time.Date(2025, 1, 1, 0, 0, 0, 0, time.UTC),
					Section:     "Repository Access",
				},
			},
		},
	}

	result, err := gen.GenerateSimpleEvidence(task, "Q1 2025", tasks)
	require.NoError(t, err)

	assert.Contains(t, result, "# ET-0001: GitHub Repository Access Controls")
	assert.Contains(t, result, "**Task Description:**")
	assert.Contains(t, result, "**Collection Window:** Q1 2025")
	assert.Contains(t, result, "## Collection Task 1: Collect repository permissions")
	assert.Contains(t, result, "**Evidence:** Teams and access levels documented.")
	assert.Contains(t, result, "**Source:** `permissions.json`")
	assert.Contains(t, result, "**Original Path:** `docs/evidence/permissions.json`")
	assert.Contains(t, result, "**Section:** Repository Access")
	assert.Contains(t, result, "> Team: Engineering")
}

