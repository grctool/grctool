func TestGetSourceMetadata(t *testing.T) {
	t.Parallel()
	tmpDir := t.TempDir()
	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator(tmpDir, log)

	testFile := filepath.Join(tmpDir, "test.json")
	require.NoError(t, os.WriteFile(testFile, []byte(`{"test": true}`), 0644))

	meta, err := gen.GetSourceMetadata(testFile)
	require.NoError(t, err)
	assert.Equal(t, testFile, meta.OriginalPath)
	assert.Equal(t, "test.json", meta.DestFilename)
	assert.Equal(t, "test.json", meta.RelativePath)
	assert.False(t, meta.LastModified.IsZero())
}

