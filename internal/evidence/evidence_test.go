func TestCopySourceFilesFlat(t *testing.T) {
	t.Parallel()
	tmpDir := t.TempDir()
	srcDir := filepath.Join(tmpDir, "src")
	destDir := filepath.Join(tmpDir, "dest")
	require.NoError(t, os.MkdirAll(srcDir, 0755))

	srcFile := filepath.Join(srcDir, "original.txt")
	require.NoError(t, os.WriteFile(srcFile, []byte("hello world"), 0644))

	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator(tmpDir, log)

	sources := []SourceFile{
		{
			OriginalPath: srcFile,
			RelativePath: "src/original.txt",
			DestFilename: "copied.txt",
		},
	}

	err := gen.CopySourceFilesFlat(sources, destDir)
	require.NoError(t, err)

	data, err := os.ReadFile(filepath.Join(destDir, "copied.txt"))
	require.NoError(t, err)
	assert.Equal(t, "hello world", string(data))
}

func TestCopySourceFilesFlat_MissingSource(t *testing.T) {
	t.Parallel()
	tmpDir := t.TempDir()
	destDir := filepath.Join(tmpDir, "dest")

	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator(tmpDir, log)

	sources := []SourceFile{
		{
			OriginalPath: "/nonexistent/file.txt",
			DestFilename: "missing.txt",
		},
	}

	// Should not error, just skip missing files
	err := gen.CopySourceFilesFlat(sources, destDir)
	require.NoError(t, err)
	// The dest file should not exist
	_, err = os.Stat(filepath.Join(destDir, "missing.txt"))
	assert.True(t, os.IsNotExist(err))
}

func TestFormatAsMarkdownQuote(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name     string
		input    string
		expected string
	}{
		{
			name:     "single line",
			input:    "Hello world",
			expected: "> Hello world\n",
		},
		{
			name:     "multi line",
			input:    "Line 1\nLine 2\nLine 3",
			expected: "> Line 1\n> Line 2\n> Line 3\n",
		},
		{
			name:     "empty string",
			input:    "",
			expected: "> \n",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()
			result := FormatAsMarkdownQuote(tt.input)
			assert.Equal(t, tt.expected, result)
		})
	}
}

func TestBreakdownIntoCollectionTasks(t *testing.T) {
	t.Parallel()
	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator("/tmp/data", log)

	t.Run("basic task", func(t *testing.T) {
		t.Parallel()
		task := &domain.EvidenceTask{
			Name:        "Test Task",
			Description: "",
			Controls:    nil,
		}
		tasks, err := gen.BreakdownIntoCollectionTasks(task)
		require.NoError(t, err)
		assert.Len(t, tasks, 1)
		assert.Contains(t, tasks[0], "Test Task")
	})

	t.Run("task with description and controls", func(t *testing.T) {
		t.Parallel()
		task := &domain.EvidenceTask{
			Name:        "GitHub Access",
			Description: "Check permissions",
			Controls:    []string{"CC6.1", "CC6.2"},
		}
		tasks, err := gen.BreakdownIntoCollectionTasks(task)
		require.NoError(t, err)
		assert.Len(t, tasks, 3)
	})
}

func TestExtractRelevantSnippets(t *testing.T) {
	t.Parallel()
	log := newTestLogger(t)
	gen := NewSimpleOutputGenerator("/tmp/data", log)

	snippet := gen.ExtractRelevantSnippets(
		"content here",
		"source.json",
		"docs/source.json",
		"Section 1",
	)

	assert.Equal(t, "content here", snippet.Content)
	assert.Equal(t, "source.json", snippet.SourceFile)
	assert.Equal(t, "docs/source.json", snippet.OriginalPath)
	assert.Equal(t, "Section 1", snippet.Section)
	assert.False(t, snippet.LastModified.IsZero())

	// Verify the content round-trips through markdown quote
	quoted := FormatAsMarkdownQuote(snippet.Content)
	assert.True(t, strings.HasPrefix(quoted, "> "))
}
